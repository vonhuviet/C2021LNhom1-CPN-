    public void NumbCheck(java.awt.event.KeyEvent evt) {
        char numb = evt.getKeyChar();
        if (!(Character.isDigit(numb)) || (numb == KeyEvent.VK_BACK_SPACE) || (numb == KeyEvent.VK_DELETE)) {
            evt.consume();
        }
    }

