    private void btnInputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInputActionPerformed
        // TODO add your handling code here:

        if (txtRollno.getText().isEmpty() || txtNamePro.getText().isEmpty() || txtNameCus.getText().isEmpty()
                || txtAddressCus.getText().isEmpty() || txtPhoneCus.getText().isEmpty() || txtWheight.getText().isEmpty()
                || txtDfee.getText().isEmpty() || txtHeight.getText().isEmpty() || txtWide.getText().isEmpty()
                || txtLength.getText().isEmpty() || txtToPrice.getText().isEmpty() || txtNote.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Bạn chưa nhập vào đơn hàng!!");
        } else {
            String Rollno = txtRollno.getText();
            String NamePro = txtNamePro.getText();
            Status Idstatus = (Status) cbxStatus.getSelectedItem();
            
            Users Usersname =  loggedID;
            
            Service IdService = (Service) cbxService.getSelectedItem();
            String NameCus = txtNameCus.getText();
            String Address = txtAddressCus.getText();
            String Phone = txtPhoneCus.getText();
            Warehouse IdWhouse = (Warehouse) cbxIDWhouse.getSelectedItem();
            float Weight = Float.parseFloat(txtWheight.getText());
            int DeliveryFee = Integer.parseInt(txtDfee.getText());
            float Height = Float.parseFloat(txtHeight.getText());
            float Wide = Float.parseFloat(txtWide.getText());
            float Lenght = Float.parseFloat(txtLength.getText());
            int Totalprice = Integer.parseInt(txtToPrice.getText());
            String Note = txtNote.getText();
            Shopinfo Idshop = (Shopinfo) cbxIdShop.getSelectedItem();

            Orders order = new Orders(NamePro, Idstatus, Usersname, IdService, Phone, Address, NameCus, IdWhouse, Weight, DeliveryFee, Height, Wide, Lenght, Totalprice, Note, Idshop, Rollno);
            OrderDAO.insert(order);
            OrderList.add(order);
            Reset();

        }


    }//GEN-LAST:event_btnInputActionPerformed

    private void btnCaculActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCaculActionPerformed
        // TODO add your handling code here:
        
        float Weight = Float.parseFloat(txtWheight.getText());
        float Height = Float.parseFloat(txtHeight.getText());
        float Wide = Float.parseFloat(txtWide.getText());
        float Lenght = Float.parseFloat(txtLength.getText());
        int DeliveryFee = Integer.parseInt(txtDfee.getText());
        int Totalprice;

        float cacul = Weight * 15000;    ///cong thuc tinh gia tien
        float ave = (Wide + Lenght + Height);
        if (ave <= 50) {
            ave = 20000;
        } else if (ave > 50 && ave <= 100) {
            ave = 50000;
        } else {
            ave = 70000;
        }
        Totalprice = (int) (DeliveryFee + ave + cacul);
        txtWheight.setText(Float.toString(Weight));
        txtDfee.setText(Integer.toString(DeliveryFee));
        txtHeight.setText(Float.toString(Height));
        txtWide.setText(Float.toString(Wide));
        txtLength.setText(Float.toString(Lenght));
        txtToPrice.setText(Integer.toString(Totalprice));

    }//GEN-LAST:event_btnCaculActionPerformed

