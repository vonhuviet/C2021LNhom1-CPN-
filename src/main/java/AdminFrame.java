    private void createBtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createBtActionPerformed
        // TODO add your handling code here:
        String username = userTXT.getText();
        String password = PwdTXT.getText();
        String confirmpwd = ConfirmPwdTXT.getText();
        String address = addresTXT.getText();
        String fullname = fullNameTXT.getText();
        String mobile = mobileTXT.getText();
        String email = emailTXT.getText();
        String Account = cbAccount.getSelectedItem().toString();
        if (!password.equals(confirmpwd)) {
            JOptionPane.showMessageDialog(rootPane, "Plz check password and confirm password");
            return;
        }
        password = Utility.Utility.getSecurityMD5(password);
        Users u;
        if (Account.equals("Active")) {
            u = new Users(username, password, 2, 1, mobile, address, fullname, email);
            UserDAO.insert(u);
            Reset();

        } else {
            u = new Users(username, password, 2, 0, mobile, address, fullname, email);
            UserDAO.insert(u);
            Reset();
        }
        showUsers();
    }//GEN-LAST:event_createBtActionPerformed

