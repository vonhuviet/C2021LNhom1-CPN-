    private void showUsers() {
        tableModel.setRowCount(0);

        for (Users u : userList) {
            tableModel.addRow(new Object[]{
                tableModel.getRowCount() + 1,
                u.getUsername(),
                u.getAddress(),
                u.getMobile(),
                u.getFullname(),
                u.getEmail(),
                u.getActive()
            });

        }
    }

